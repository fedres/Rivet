// Write the generated toolchain file. CMake reads CACHE-tier variables
// before kicking off compiler detection, so pinning compiler + linker +
// (on Apple) sysroot here means user CMakeLists never has a chance to
// reach for the system compiler.
std::string make_toolchain_text(const toolchain::ToolchainInfo& tc,
                                 const Path& extra_prefix) {
    // generic_string() = forward slashes; safe inside CMake double-quoted
    // string literals on every host.
    auto path = [](const Path& p) { return p.generic_string(); };

    std::string text =
        "# Generated by `rivet build` — drives the user's CMakeLists with\n"
        "# rivet's bundled clang + lld + ninja. Edit rivet's source if you\n"
        "# need to change anything here; this file is overwritten on every\n"
        "# `rivet build`.\n\n";

#if defined(_WIN32)
    text += std::format(
        "set(CMAKE_C_COMPILER       \"{}\" CACHE FILEPATH \"\")\n"
        "set(CMAKE_CXX_COMPILER     \"{}\" CACHE FILEPATH \"\")\n"
        "set(CMAKE_AR               \"{}\" CACHE FILEPATH \"\")\n"
        "set(CMAKE_LINKER           \"{}\" CACHE FILEPATH \"\")\n"
        "set(CMAKE_RC_COMPILER      \"{}\" CACHE FILEPATH \"\")\n"
        "set(CMAKE_MT               \"{}\" CACHE FILEPATH \"\")\n"
        "set(CMAKE_MSVC_RUNTIME_LIBRARY \"MultiThreaded$<$<CONFIG:Debug>:Debug>\" "
            "CACHE STRING \"\")\n",
        path(tc.clang_cl()), path(tc.clang_cl()),
        path(tc.llvm_lib()), path(tc.lld_link()),
        path(tc.root / "bin" / "llvm-rc.exe"),
        path(tc.root / "bin" / "llvm-mt.exe"));
#else
    text += std::format(
        "set(CMAKE_C_COMPILER   \"{}\" CACHE FILEPATH \"\")\n"
        "set(CMAKE_CXX_COMPILER \"{}\" CACHE FILEPATH \"\")\n"
        "set(CMAKE_AR           \"{}\" CACHE FILEPATH \"\")\n"
        "set(CMAKE_RANLIB       \"{}\" CACHE FILEPATH \"\")\n"
        "set(CMAKE_LINKER       \"{}\" CACHE FILEPATH \"\")\n",
        path(tc.clang()), path(tc.clangpp()),
        path(tc.llvm_ar()), path(tc.llvm_ranlib()), path(tc.lld()));
#endif

    text +=
        "foreach(_v EXE SHARED MODULE)\n"
        "    set(CMAKE_${_v}_LINKER_FLAGS_INIT \"-fuse-ld=lld\")\n"
        "endforeach()\n";

    text +=
        "if(APPLE AND NOT CMAKE_OSX_SYSROOT)\n"
        "    execute_process(COMMAND xcrun --show-sdk-path\n"
        "                    OUTPUT_VARIABLE _rivet_sdk_path\n"
        "                    OUTPUT_STRIP_TRAILING_WHITESPACE\n"
        "                    RESULT_VARIABLE _rivet_sdk_rc)\n"
        "    if(_rivet_sdk_rc EQUAL 0 AND EXISTS \"${_rivet_sdk_path}\")\n"
        "        set(CMAKE_OSX_SYSROOT \"${_rivet_sdk_path}\" CACHE PATH \"\")\n"
        "    endif()\n"
        "endif()\n";

    // Per-project package install tree from `rivet add ...` / `rivet fetch`.
    // CMAKE_PREFIX_PATH is what find_package() walks — putting the rivet
    // tree there makes find_package(fmt CONFIG REQUIRED) work without
    // the user adding any rivet-specific incantation.
    if (!extra_prefix.empty()) {
        text += std::format(
            "list(PREPEND CMAKE_PREFIX_PATH \"{}\")\n", path(extra_prefix));
    }

    // find_package interception. Every TOP-LEVEL find_package() call from
    // the user's CMakeLists is logged to <build_dir>/rivet-find-package.log.
    // After configure, rivet reads it and surfaces a list of packages the
    // project asked for — useful both for diagnosing "what do I need to
    // `rivet add`?" and for the future `rivet add --from-find-package`
    // bulk-import flow.
    //
    // Recursion guard: when our override calls `_find_package`, that may
    // dive through internal CMake modules that themselves use
    // find_package(...) (e.g. CheckCXXCompilerFlag, Threads probing).
    // Without the guard `_find_package` re-enters our macro and CMake
    // hits its 1000-depth recursion limit. The `RIVET_IN_FIND_PACKAGE`
    // flag scopes logging to outer calls only.
    text +=
        "macro(find_package _rivet_pkg)\n"
        "    if(NOT DEFINED RIVET_IN_FIND_PACKAGE)\n"
        "        set(RIVET_IN_FIND_PACKAGE TRUE)\n"
        "        file(APPEND \"${CMAKE_BINARY_DIR}/rivet-find-package.log\"\n"
        "             \"${_rivet_pkg}\\n\")\n"
        "        _find_package(${ARGV})\n"
        "        unset(RIVET_IN_FIND_PACKAGE)\n"
        "    else()\n"
        "        _find_package(${ARGV})\n"
        "    endif()\n"
        "endmacro()\n";

    return text;
}

int spawn_and_wait(rivet::process::SpawnOptions opts, std::string_view label) {
    std::cout << "$ " << label << "\n";
    auto child = rivet::process::spawn(std::move(opts));
    if (!child) {
        std::cerr << "error: " << child.error().message << "\n";
        return -1;
    }
    auto code = child->wait();
    return code.value_or(-1);
}

