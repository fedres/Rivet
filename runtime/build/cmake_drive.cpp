Result<void> build_via_cmake(const CmakeDriveOptions& opts,
                              const toolchain::ToolchainInfo& tc) {
    // Project state under .rivet/cmake/ — same root as rivet's own builds
    // so a project that has BOTH a rivet.toml and a CMakeLists.txt can
    // mix them without stepping on each other.
    Path build_dir = opts.project_dir / ".rivet" / "cmake";
    RIVET_TRY(rivet::fs::create_dirs(build_dir));

    // Drop the toolchain file.
    Path toolchain_file = build_dir / "RivetToolchain.cmake";
    auto text = make_toolchain_text(tc, opts.extra_prefix);
    rivet::ByteSpan span{
        reinterpret_cast<const std::byte*>(text.data()), text.size()};
    RIVET_TRY(rivet::fs::write_atomic(toolchain_file, span));

    // ── cmake configure ────────────────────────────────────────────────
    rivet::process::SpawnOptions cfg;
    cfg.args = {
        "cmake",
        "-S", opts.project_dir.string(),
        "-B", build_dir.string(),
        "-G", "Ninja",
        std::format("-DCMAKE_TOOLCHAIN_FILE={}", toolchain_file.generic_string()),
        std::format("-DCMAKE_BUILD_TYPE={}",
            opts.profile == "release" ? "Release" :
            opts.profile == "debug"   ? "Debug"   :
            opts.profile == "asan"    ? "Debug"   :
            "Debug"),
        std::format("-DCMAKE_MAKE_PROGRAM={}", tc.ninja().generic_string()),
    };
    for (const auto& d : opts.defines) cfg.args.push_back("-D" + d);
    cfg.inherit_env    = true;
    cfg.capture_stdout = false;
    cfg.capture_stderr = false;

    if (int rc = spawn_and_wait(std::move(cfg), "cmake configure"); rc != 0)
        return make_error("cmake configure failed (exit " + std::to_string(rc) + ")");

    // ── cmake --build ──────────────────────────────────────────────────
    rivet::process::SpawnOptions build;
    build.args = {"cmake", "--build", build_dir.string(), "--parallel"};
    build.inherit_env    = true;
    build.capture_stdout = false;
    build.capture_stderr = false;
    if (int rc = spawn_and_wait(std::move(build), "cmake build"); rc != 0)
        return make_error("cmake build failed (exit " + std::to_string(rc) + ")");

    // Surface find_package() hits the user's CMakeLists requested.
    // Helps the "I cloned a cmake project — what do I need to rivet add?"
    // question without forcing the user to grep cmake's stderr.
    Path fp_log = build_dir / "rivet-find-package.log";
    if (auto bytes = rivet::fs::read_file(fp_log)) {
        std::string_view text(reinterpret_cast<const char*>(bytes->data()), bytes->size());
        std::unordered_set<std::string> seen;
        std::vector<std::string> uniq;
        size_t pos = 0;
        while (pos < text.size()) {
            size_t nl = text.find('\n', pos);
            std::string line(text.substr(pos, (nl == std::string_view::npos ? text.size() : nl) - pos));
            // Strip CR (Windows) + whitespace.
            while (!line.empty() && (line.back() == '\r' || line.back() == ' ' || line.back() == '\t'))
                line.pop_back();
            if (!line.empty() && seen.insert(line).second) uniq.push_back(line);
            if (nl == std::string_view::npos) break;
            pos = nl + 1;
        }
        if (!uniq.empty()) {
            std::cout << "\n  find_package() requests this project made:\n";
            for (const auto& p : uniq)
                std::cout << "    - " << p << "  (try: rivet add " << p << ")\n";
        }
    }

    std::cout << "\n  Build dir: " << build_dir.string() << "\n";
    return {};
}

